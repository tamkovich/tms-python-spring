# Решить квадратное уравнения
a = int(input("a = "))
b = int(input("b = "))
c = int(input("c = "))
D = b ** 2 - 4 * a * c
if D < 0:
    print("Уравнения не имеет корень")
elif D == 0:
    x = -b / (2 * a)
    print(f"Уравнения имеет один корень:  x1 = x2 = {x}")
else:
    x1 = (-b + D ** 0.5) / (2 * a)
    print(x1)
    x2 = (-b - D ** 0.5) / (2 * a)
    print(f"Уравнения имеет два корня:  x1 = {x1},  x2 = {x2}")
