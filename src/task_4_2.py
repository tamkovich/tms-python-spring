# Дан список целых чисел. Подсчитать сколько четных чисел в списке

list_1, rez, a = [1, 2, 3, 4, 5, 6], 0, 0

# ------------------------WHILE----------------------------------------

while a < len(list_1):
    if list_1[a] % 2 == 0:
        rez += 1
    a += 1
print(rez)

# -------------------------FOR-----------------------------------------

rez = 0
for i in range(len(list_1)):
    if list_1[i] % 2 == 0:
        rez += 1
print(rez)
