# task_1_4: Даны два числа. Найти среднее арифметическое и среднее геометрическое этих чисел
import math

number_1 = 8
number_2 = 202
sr_arifm = (number_1 + number_2) / 2
sr_geom = math.sqrt(number_1 * number_2)
print(f"Среднее арифметическое = {sr_arifm}")
print(f"Cреднее геометрическое = {sr_geom}")
