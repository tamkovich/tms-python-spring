# Составить список чисел Фибоначчи содержащий 15 элементов.

fib, i = [0, 1], 2

# ------------------------WHILE----------------------------------------

while i < 15:
    fib.insert(i, fib[i - 1] + fib[i - 2])
    i += 1
print(fib)


fib = [0, 1]

# -------------------------FOR-----------------------------------------

for i in range(15):
    if i > 1:
        fib.insert(i, fib[i - 1] + fib[i - 2])
print(fib)
