class Math:
    def __init__(self, arg1, arg2):
        self.arg1 = arg1
        self.arg2 = arg2

    def sumarize(self):
        return self.arg1 + self.arg2

    def diff(self):
        return self.arg1 - self.arg2

    def div(self):
        return self.arg1 / self.arg2

    def multiply(self):
        return self.arg1 * self.arg2
