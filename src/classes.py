class Math:
    def __init__(self, a, b):
        self.a = a
        self.b = b

    def add(self) -> float:
        return self.a + self.b

    def subtract(self) -> float:
        return self.a - self.b

    def multiply(self) -> float:
        return self.a * self.b

    def divide(self) -> float:
        return self.a / self.b
